calificacion = int(input("Ingresa tu calificación (9, 10, 11 o 12): "))

if calificacion == 9:
    print("Primer año")
elif calificacion == 10:
    print("Segundo año")
elif calificacion == 11:
    print("Junior")
elif calificacion == 12:
    print("Último año")
else:
    print("Por determinar")